package hashset;

import java.util.HashSet;

public class Hashset {

	public static void main(String[] args) {
		HashSet<String> hs=new HashSet<String>();
		hs.add("k");
		hs.add("G");
		hs.add("O");
		hs.add("T");
		hs.add(" ");
		hs.add("A");
		hs.add("D");
		hs.add("S");
		hs.add(" ");
		hs.add("k");
		
		System.out.println(hs);

	}

}
